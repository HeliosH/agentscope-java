package io.agentscope.saas.app.config;

import com.fasterxml.jackson.databind.DeserializationFeature;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.SerializationFeature;
import com.fasterxml.jackson.datatype.jsr310.JavaTimeModule;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

/**
 * Provides the shared {@link ObjectMapper} for the persistence layer (content_json serialization,
 * marketplace config) and any other component that needs a consistent codec. Configured for
 * forward-compatible JSON (unknown properties ignored, Java 8 time types as ISO strings) so
 * content blocks and config entries round-trip cleanly across the sealed {@code ContentBlock}
 * hierarchy.
 */
@Configuration
public class JacksonConfig {

    @Bean
    public ObjectMapper objectMapper() {
        return new ObjectMapper()
                .registerModule(new JavaTimeModule())
                .disable(SerializationFeature.WRITE_DATES_AS_TIMESTAMPS)
                .configure(DeserializationFeature.FAIL_ON_UNKNOWN_PROPERTIES, false);
    }
}
